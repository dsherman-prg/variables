fun main(args: Array<String>){
    // Create variables
    var myByte: Byte = 7
    var myShort: Short = 98
    var myInt: Int = 10
    var myLong: Long = 40234274L
    var myFloat: Float = .1F
    var myDouble: Double = 234234.2234
    var myBoolean: Boolean = false
    var myChar: Char = 'D'

    // Assign new values to the variables
    myByte = 6
    myShort = 97
    myInt = 9
    myLong = 40234233
    myFloat = .09F
    myDouble = 234234.2233
    myBoolean = true
    myChar = 'C'

    // Print the variables
    println("Byte Example: $myByte")
    println("Short Example: $myShort")
    println("Int Example: $myInt")
    println("Long Example: $myLong")
    println("Float Example: $myFloat")
    println("Double Example: $myDouble")
    println("Boolean Example: $myBoolean")
    println("Char Example: $myChar")
}